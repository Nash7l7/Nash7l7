nilai = int(input("Masukkan nilai Anda: "))
if (nilai<40):
   print ("Grade Anda adalah F")
else:
   if (nilai<50):
      print ("Grade Anda adalah E")
   else:
      if (nilai<60):
         print ("Grade Anda adalah D")
      else:
         if (nilai<70):
            print ("Grade Anda adalah C")
         else:
            if (nilai<80):
               print ("Grade Anda adalah B")
            else:
               print ("Grade Anda adalah A")
